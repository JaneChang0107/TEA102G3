package com.rentDetail.model;

public class RentDetailVO {
	private String rd_id;
	private String ro_id;
	private String r_id;
	private Integer rd_count;

	public RentDetailVO() {
	}

	public String getRd_id() {
		return rd_id;
	}

	public void setRd_id(String rd_id) {
		this.rd_id = rd_id;
	}

	public String getRo_id() {
		return ro_id;
	}

	public void setRo_id(String ro_id) {
		this.ro_id = ro_id;
	}

	public String getR_id() {
		return r_id;
	}

	public void setR_id(String r_id) {
		this.r_id = r_id;
	}

	public Integer getRd_count() {
		return rd_count;
	}

	public void setRd_count(Integer rd_count) {
		this.rd_count = rd_count;
	}

}
